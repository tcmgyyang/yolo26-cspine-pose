class DPCA(nn.Module):
    """DPCA channel-attention module used by the proposed pose model.

    Design:
    1. Efficient channel attention built on top of ECA.
    2. Added spatial-pyramid pooling branch to capture multi-scale features.
    3. Tuned for keypoint detection — strengthens response to local detail.

    Compared with ECA:
    - ECA: global average pooling only.
    - DPCA: global + multi-scale local pooling.
    """
    
    def __init__(self, c1, k_size=3):
        super().__init__()
        self.gap = nn.AdaptiveAvgPool2d(1)  # 全局平均池化
        self.lap = nn.AdaptiveAvgPool2d(3)  # 局部池化 3x3
        
        # 通道注意力
        self.conv_global = nn.Conv1d(1, 1, kernel_size=k_size, padding=k_size // 2, bias=False)
        self.conv_local = nn.Conv1d(1, 1, kernel_size=k_size, padding=k_size // 2, bias=False)
        
        # 融合权重（可学习）
        self.alpha = nn.Parameter(torch.ones(1) * 0.5)
        self.sigmoid = nn.Sigmoid()
    
    def forward(self, x):
        b, c, h, w = x.size()
        
        # 全局分支
        y_global = self.gap(x).view(b, 1, c)
        y_global = self.conv_global(y_global).view(b, c, 1, 1)
        
        # 局部分支 - 捕捉关键点局部细节
        y_local = self.lap(x)  # (b, c, 3, 3)
        y_local = y_local.view(b, c, -1).mean(dim=-1, keepdim=True).view(b, 1, c)
        y_local = self.conv_local(y_local).view(b, c, 1, 1)
        
        # 自适应融合
        alpha = self.sigmoid(self.alpha)
        y = alpha * y_global + (1 - alpha) * y_local
        
        return x * self.sigmoid(y)


class EfficientChannelAttention(nn.Module):
    """
    Efficient Channel Attention (ECA) 模块
    
    论文: ECA-Net: Efficient Channel Attention for Deep Convolutional Neural Networks
    一种轻量级的通道注意力机制，通过1D卷积实现跨通道交互
    
    Args:
        c: 输入通道数
        b: 用于计算卷积核大小的参数，默认为1
        gamma: 用于计算卷积核大小的参数，默认为2
    """
    def __init__(self, c, b=1, gamma=2):
        super(EfficientChannelAttention, self).__init__()
        t = int(abs((math.log(c, 2) + b) / gamma))
        k = t if t % 2 else t + 1

        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.conv1 = nn.Conv1d(1, 1, kernel_size=k, padding=int(k/2), bias=False)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        out = self.avg_pool(x)
        out = self.conv1(out.squeeze(-1).transpose(-1, -2)).transpose(-1, -2).unsqueeze(-1)
        out = self.sigmoid(out)
        return out * x

